package com.nws.controlstatement;

public class Statementswitch {

    public static void main(String args[]) {
        char choice = 'B';
        switch (choice) {
            case 'A':
                System.out.println("You have choosen A");
                break;

            case 'B':
                System.out.println("You have choosen B");
                break;
                
            case 'C':
                System.out.println("You have choosen C");
                break;
        }
    }
}
